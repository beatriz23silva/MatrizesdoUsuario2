#ifndef FUNCOES_H
#define FUNCOES_H

/**
 * @brief Opcao
 * @param opcao
 * Permite que o usuário selecione a operação a ser realizada.
 */

void Opcao(int opcao);

/**
 * @brief menu
 * Exibe o menu de opções disponíveis para o usuário.
 */
void menu();

/**
 * @brief tamMatrizA
 * Utilizada para definir o tamanho da matriz A.
 */
void tamMatrizA();

/**
 * @brief tamMatrizB
 * Utilizada para definir o tamanho da matriz B.
 */
void tamMatrizB();

/**
 * @brief num_aleatoriosA
 * Preenche a matriz A com valores aleatórios.
 */
void num_aleatoriosA();
/**
 * @brief num_aleatoriosB
 * Preenche a matriz B com valores aleatórios.
 */
void num_aleatoriosB();

/**
 * @brief atribuir_A
 * Utilizada para atribuir valores aos elementos da matriz A.
 */
void atribuir_A();
/**
 * @brief atribuir_B
 * Utilizada para atribuir valores aos elementos da matriz B.
 */
void atribuir_B();
/**
 * @brief soma
 * Utilizada para realizar a soma entre as matrizes A e B, cujo resultado é armazenado na matriz C.
 */
void soma();
/**
 * @brief sub
 * Utilizada para realizar a subtração entre as matrizes A e B, cujo resultado é armazenado na matriz C.
 */
void sub();
/**
 * @brief mult
 * Utilizada para realizar a multiplicação entre as matrizes A e B, cujo resultado é armazenado na matriz C.
 */
void mult();
/**
 * @brief ImprimirA
 * Imprime para o usuário a matriz A.
 */
void ImprimirA();
/**
 * @brief ImprimirB
 * Imprime para o usuário a matriz B.
 */
void ImprimirB();
/**
 * @brief ImprimirC
 * Imprime para o usuário a matriz C.
 */
void ImprimirC();
/**
 * @brief LerarquivoMatrizA
 */
void LerarquivoMatrizA();
/**
 * @brief LerarquivoMatrizB
 */
void LerarquivoMatrizB();

/**
 * @brief LerarquivoMatrizC
 */
void LerarquivoMatrizC();
/**
 * @brief EscreverarquivoMatrizA
 */
void EscreverarquivoMatrizA();
/**
 * @brief EscreverarquivoMatrizB
 */
void EscreverarquivoMatrizB();
/**
 * @brief EscreverarquivoMatrizC
 */
void EscreverarquivoMatrizC();





#endif // FUNCOES_H

